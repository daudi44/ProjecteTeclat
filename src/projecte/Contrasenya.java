    private void llegirFitxerConfig() {
        BufferedReader configIn = null;
        PrintWriter configOut = null;

        try {
            if (fitxerConfig.exists()) {
                configIn = new BufferedReader(new FileReader(fitxerConfig));
                configIn.readLine();
                passwd = configIn.readLine().strip();
            } else {
                configOut = new PrintWriter(fitxerConfig);
                configOut.println(10000);
                configOut.print("passwd");
                throw new NumberFormatException();
            }

        } catch (NumberFormatException | NullPointerException | IOException ex) {
            JOptionPane.showMessageDialog(this, "Atenci贸, problema en el fitxer de configuraci贸");
            passwd = "passwd";
            //dades = new Teclat[100];
        } finally {
            try {
                if (configIn != null) {
                    configIn.close();
                }
                if (configOut != null) {
                    configOut.close();
                }
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Atenci贸, problema en el fitxer de configuraci贸");
            }
        }
    }

